    private void jLabel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con1=DriverManager.getConnection("jdbc:mysql://localhost:3306/database_cases","root","root");
            String sql="Insert into casehistory(idcase, casehistory_note, date,officer_idch) values(?,?,?,'"+Login_Officer.officer_id+"')";
            
            PreparedStatement pst1=con1.prepareStatement(sql);
            
            //pst1.setString(1, officer_id.getText());
            pst1.setString(1, (String) jComboBox1.getSelectedItem());
            pst1.setString(2, caseHistory_note.getText());
            pst1.setString(3, date.getText());
            pst1.execute();
            //JOptionPane.showMessageDialog(null, "Officer Added Successfully");

            JOptionPane.showMessageDialog(null, "Case History Added Successfully");
            CaseHistory_Add cha = new CaseHistory_Add();
            cha.setVisible(true);
            setVisible(false);
            //officer_id.setText("");
            caseHistory_note.setText("");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jLabel8MouseClicked

